  function normalizeEntryForm(lemma, entry){
    const profile = detectRegularContractProfile(lemma);
    if (!profile || !entry || !entry.form) return entry && entry.form;
    const familyRules = RULES[profile];
    if (!familyRules) return entry.form;
    if (entry.kind === 'finite') {
      const tenseRules = familyRules.finite[entry.tense];
      const voice = entry.voice === 'middle' || entry.voice === 'passive' ? entry.voice : (entry.voice === 'middle/passive' ? 'middle/passive' : entry.voice);
      const voiceRules = tenseRules && tenseRules[voice];
      return voiceRules ? applyRules(entry.form, voiceRules) : entry.form;
    }
    if (entry.kind === 'infinitive') {
      const voice = entry.voice === 'middle' || entry.voice === 'passive' ? entry.voice : (entry.voice === 'middle/passive' ? 'middle/passive' : entry.voice);
      const rules = familyRules.infinitive[voice];
      return rules ? applyRules(entry.form, rules) : entry.form;
    }
    if (entry.kind === 'participle') {
      const voice = entry.voice === 'middle' || entry.voice === 'passive' ? entry.voice : (entry.voice === 'middle/passive' ? 'middle/passive' : entry.voice);
      const rules = familyRules.participle[voice];
      return rules ? applyRules(entry.form, rules) : entry.form;
    }
    return entry.form;
  }

